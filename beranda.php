          <h3>Beasiswa</h3>
          <p>
            &nbsp;&nbsp;&nbsp;&nbsp; Beasiswa adalah dukungan finansial bagi mahasiswa yang berprestasi atau memenuhi kriteria tertentu. Program ini bertujuan untuk mendorong semangat belajar, mengembangkan potensi, dan memberikan kesempatan pendidikan bagi mereka yang terkendala ekonomi. Beasiswa diberikan berdasarkan berbagai kriteria, seperti prestasi akademik, non-akademik, atau kebutuhan finansial, untuk membantu penerima berkontribusi positif pada masyarakat.
          </p>

          <h5>1. Beasiswa Akademik</h5>
          <p>
            &nbsp;&nbsp;&nbsp;&nbsp; Beasiswa Akademik diberikan kepada mahasiswa yang menunjukkan prestasi luar biasa di bidang akademik. Beasiswa ini ditujukan untuk mendukung mahasiswa berprestasi agar dapat terus mengembangkan potensi akademis mereka.
          </p>
          <p>Adapun syarat-syarat yang dibutuhkan adalah:</p>
          <ul>
            <li>Diberikan kepada mahasiswa dengan prestasi akademik yang unggul.</li>
            <li>Memiliki IPK minimal 3.0</li>
            <li>Melampirkan surat rekomendasi dari dosen.</li>
          </ul>

          <h5>2. Beasiswa Non-akademik</h5>
          <p>
            &nbsp;&nbsp;&nbsp;&nbsp; Beasiswa Non-Akademik ditujukan bagi mahasiswa yang memiliki bakat atau prestasi di bidang non-akademik, seperti seni, olahraga, atau kegiatan kepemimpinan. Penerima beasiswa ini diharapkan mampu mengembangkan kemampuan di luar akademik dan memberikan kontribusi dalam bidangnya.
          </p>
          <p>Adapun syarat-syarat yang dibutuhkan adalah:</p>
          <ul>
            <li>Diberikan kepada mahasiswa yang memiliki bakat atau prestasi di bidang non-akademik seperti seni, olahraga, atau kepemimpinan.</li>
            <li>Melampirkan sertifikat atau piagam penghargaan dari kompetisi atau kegiatan terkait.</li>
            <li>Memiliki IPK minimal 3.0</li>
            <li>Mendapatkan rekomendasi dari pembimbing atau pelatih di bidang yang diikuti.</li>
          </ul>
